package assignment_19_6;

public class Student {
	private String nameString;
	private int age;
	
	public Student(String nameString, int age) {
		super();
		this.nameString = nameString;
		this.age = age;
	}

	public String getNameString() {
		return nameString;
	}

	public int getAge() {
		return age;
	}
}
